package repl;

public class Repl091 {
    public static void main(String[] args) {
        char v = 'v';
        char z = 'z';
        char y = 'y';
        char x = 'x';
        char w ='w';
        for (char overAll = z; overAll >=v; overAll--){
            if (overAll== 'y') {
                for (char i = z; i >= v; i--){
                    System.out.println(z+""+ i); }
            }if (overAll== 'y') {
                for (char l = z; l >= v; l--){
                    System.out.println(y+""+ l); }
            }if (overAll== 'x') {
                for (char k = z; k >= v; k--){
                    System.out.println('x'+""+ k); }
            }if (overAll== 'w') {
                for (char t = z; t >= v; t--){
                    System.out.println(w+""+ t); }
            }if (overAll== 'y') {
                for (char n = z; n >= v; n--){
                    System.out.println(v+""+ n); }
            }







        }
    }
}
